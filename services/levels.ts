export interface GameLevel {
  level: number;
  sentences: string[];
  tier: string;
  tierLabel: string;
  tierLabelAr: string;
  tierColor: string;
  stars: number;
  locked: boolean;
  score: number;
}

interface TierInfo {
  tier: string;
  tierLabel: string;
  tierLabelAr: string;
  tierColor: string;
}

export function getTierForLevel(level: number): TierInfo {
  if (level <= 5)
    return {
      tier: "beginner",
      tierLabel: "Beginner",
      tierLabelAr: "مُبْتَدِئ",
      tierColor: "#22C55E",
    };
  if (level <= 10)
    return {
      tier: "elementary",
      tierLabel: "Elementary",
      tierLabelAr: "أَسَاسِيّ",
      tierColor: "#34D399",
    };
  if (level <= 15)
    return {
      tier: "intermediate",
      tierLabel: "Intermediate",
      tierLabelAr: "مُتَوَسِّط",
      tierColor: "#3B82F6",
    };
  if (level <= 20)
    return {
      tier: "advanced",
      tierLabel: "Advanced",
      tierLabelAr: "مُتَقَدِّم",
      tierColor: "#8B5CF6",
    };
  if (level <= 25)
    return {
      tier: "expert",
      tierLabel: "Expert",
      tierLabelAr: "خَبِير",
      tierColor: "#F59E0B",
    };
  return {
    tier: "master",
    tierLabel: "Master",
    tierLabelAr: "مُتَفَوِّق",
    tierColor: "#EF4444",
  };
}

function makeLevel(level: number, sentences: string[]): GameLevel {
  return {
    level,
    sentences,
    ...getTierForLevel(level),
    stars: 0,
    locked: level !== 1,
    score: 0,
  };
}

