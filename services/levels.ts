export function getGameLevel(level: number): GameLevel {
  const found = GAME_LEVELS.find((l) => l.level === level);
  if (!found) throw new Error(`Level ${level} not found`);
  return { ...found };
}

export interface GameProgress {
  currentLevel: number;
  levels: { [level: number]: { stars: number; score: number } };
}

export async function getUserGameProgress(): Promise<GameProgress> {
  try {
    const raw = await AsyncStorage.getItem(GAME_PROGRESS_KEY);
    if (raw) {
      const parsed = JSON.parse(raw) as GameProgress;
      if (parsed.currentLevel && parsed.levels) return parsed;
    }
  } catch {}
  return { currentLevel: 1, levels: {} };
}

export async function saveGameLevelResult(
  level: number,
  score: number
): Promise<{ stars: number; unlocked: boolean }> {
  const progress = await getUserGameProgress();

  let stars = 0;
  if (score >= 90) stars = 3;
  else if (score >= 70) stars = 2;
  else if (score >= 50) stars = 1;

  const prev = progress.levels[level];
  const bestStars = prev ? Math.max(prev.stars, stars) : stars;
  const bestScore = prev ? Math.max(prev.score, score) : score;
  progress.levels[level] = { stars: bestStars, score: bestScore };

  let unlocked = false;
  if (score >= 50 && level < 30) {
    const nextLevel = level + 1;
    if (!progress.levels[nextLevel]) {
      progress.levels[nextLevel] = { stars: 0, score: 0 };
    }
    if (progress.currentLevel < nextLevel) {
      progress.currentLevel = nextLevel;
    }
    unlocked = true;
  }

  await AsyncStorage.setItem(GAME_PROGRESS_KEY, JSON.stringify(progress));
  syncLevelToCloud(`game_level_${level}`).catch(() => {});

  return { stars, unlocked };
}

